async function createUser(email, password, role, fullName, clientId, gatewayId) {
  try {
    let userId;
    let userExists = false;

    // Verificar se usu√°rio j√° existe
    const { data: { users }, error: listError } = await supabase.auth.admin.listUsers();
    if (listError) {
      throw new Error(`Erro ao listar usu√°rios: ${listError.message}`);
    }

    const existingUser = users.find(u => u.email === email);

    if (existingUser) {
      console.log(`      ‚ö†Ô∏è  ${email} j√° existe`);
      userId = existingUser.id;
      userExists = true;
    } else {
      // Criar usu√°rio
      const { data: authData, error: authError } = await supabase.auth.admin.createUser({
        email,
        password,
        email_confirm: true,
      });

      if (authError) {
        throw new Error(`Erro ao criar ${email}: ${authError.message}`);
      }
      
      userId = authData.user.id;
      console.log(`      ‚úÖ ${email} criado`);
    }

    // Criar/atualizar perfil
    const profileData = {
      id: userId,
      role,
      client_id: clientId,
      full_name: fullName,
      updated_at: new Date().toISOString()
    };

    if (gatewayId) {
      profileData.gateway_id = gatewayId;
    }

    const { error: profileError } = await supabase
      .from('profiles')
      .upsert(profileData);

    if (profileError) {
      throw new Error(`Erro ao criar perfil para ${email}: ${profileError.message}`);
    }

    console.log(`      ‚úÖ Perfil ${role} configurado`);

  } catch (error) {
    throw new Error(`Falha ao criar usu√°rio ${email}: ${error.message}`);
  }
}

async function finalVerification() {
  try {
    // Verificar tabelas
    const { data: tables, error: tableError } = await supabase
      .from('information_schema.tables')
      .select('table_name')
      .eq('table_schema', 'public')
      .order('table_name');
    
    if (!tableError && tables) {
      console.log(`   ‚úÖ ${tables.length} tabelas criadas`);
    }

    // Verificar cliente
    const { data: client, error: clientError } = await supabase
      .from('clients')
      .select('id, name')
      .eq('id', '0ec3137d-ee68-4aba-82de-143b3c61516a')
      .single();

    if (!clientError && client) {
      console.log(`   ‚úÖ Cliente: ${client.name}`);
    }

    // Verificar gateway
    const { data: gateway, error: gatewayError } = await supabase
      .from('gateways')
      .select('id, name, type')
      .eq('id', '7e7e93d9-fc1a-4ae0-b7ab-775494d57cad')
      .single();

    if (!gatewayError && gateway) {
      console.log(`   ‚úÖ Gateway: ${gateway.name} (${gateway.type})`);
    }

    // Verificar usu√°rios
    const { data: profiles, error: profilesError } = await supabase
      .from('profiles')
      .select('role, full_name')
      .in('role', ['admin', 'gateway_user']);

    if (!profilesError && profiles) {
      console.log(`   ‚úÖ ${profiles.length} usu√°rios configurados`);
    }

  } catch (error) {
    console.log(`   ‚ö†Ô∏è  Erro na verifica√ß√£o: ${error.message}`);
  }
}


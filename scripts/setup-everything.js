async function executeSqlScript() {
  try {
    console.log('   üìÑ Lendo script SQL...');
    const sqlPath = path.join(__dirname, 'setup-complete-system.sql');
    const sqlContent = fs.readFileSync(sqlPath, 'utf8');
    
    console.log('   üîß Executando comandos SQL...');
    
    // Dividir o SQL em comandos individuais
    const commands = sqlContent
      .split(';')
      .map(cmd => cmd.trim())
      .filter(cmd => 
        cmd.length > 0 && 
        !cmd.startsWith('--') && 
        !cmd.startsWith('SELECT \'') &&
        !cmd.includes('COMMIT') &&
        !cmd.includes('status')
      );

    let successCount = 0;
    let errorCount = 0;

    for (const command of commands) {
      if (command.includes('information_schema') || command.includes('pg_type')) {
        continue; // Pular comandos de verifica√ß√£o
      }

      try {
        // Executar comando SQL diretamente
        const response = await fetch(`${supabaseUrl}/rest/v1/rpc/exec_sql`, {
          method: 'POST',
          headers: {
            'Content-Type': 'application/json',
            'Authorization': `Bearer ${supabaseServiceRoleKey}`,
            'apikey': supabaseServiceRoleKey
          },
          body: JSON.stringify({ sql_query: command })
        });

        if (!response.ok) {
          // Se a fun√ß√£o RPC n√£o existir, pular este comando
          if (response.status === 404) {
            console.log(`   ‚ö†Ô∏è  Pulando comando SQL (RPC n√£o dispon√≠vel): ${command.substring(0, 50)}...`);
            continue;
          }
          
          const errorText = await response.text();
          if (errorText.includes('already exists') ||
              errorText.includes('duplicate key') ||
              errorText.includes('does not exist')) {
            console.log(`   ‚ö†Ô∏è  Ignorando (j√° existe): ${command.substring(0, 50)}...`);
          } else {
            console.log(`   ‚ùå Erro: ${errorText.substring(0, 100)}`);
            errorCount++;
          }
        } else {
          successCount++;
          if (command.includes('CREATE TABLE')) {
            const tableName = command.match(/CREATE TABLE.*?(\w+)\s*\(/)?.[1];
            console.log(`   ‚úÖ Tabela: ${tableName}`);
          } else if (command.includes('CREATE TYPE')) {
            const typeName = command.match(/CREATE TYPE.*?(\w+)\s+AS/)?.[1];
            console.log(`   ‚úÖ Tipo: ${typeName}`);
          } else if (command.includes('INSERT INTO')) {
            const tableName = command.match(/INSERT INTO.*?(\w+)\s*\(/)?.[1];
            console.log(`   ‚úÖ Dados: ${tableName}`);
          } else if (command.includes('CREATE INDEX')) {
            const indexName = command.match(/CREATE INDEX.*?(\w+)\s+ON/)?.[1];
            console.log(`   ‚úÖ √çndice: ${indexName}`);
          } else if (command.includes('CREATE POLICY')) {
            console.log(`   ‚úÖ Pol√≠tica de seguran√ßa criada`);
          }
        }
      } catch (err) {
        console.log(`   ‚ùå Erro inesperado: ${err.message.substring(0, 100)}`);
        errorCount++;
      }
    }

    console.log(`   üìä Resumo: ${successCount} sucessos, ${errorCount} erros`);
    
  } catch (error) {
    throw new Error(`Falha ao executar script SQL: ${error.message}`);
  }
}

